void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    stack<char> st;
    int regB = 0;

    for (const auto &c : s)
    {
        if (!st.empty() && c == ')' && st.top() == '(')
        {
            regB++;
            st.pop();
        }
        else
        {
            st.push(c);
        }
    }
    cout << (n / 2) - regB << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

