void solve()
{
    ll N, D;
    cin >> N >> D;

    vector<ll> arr(N, 0);
    for (ll i = 0; i < N; i++)
    {
        cin >> arr[i];
    }
    ll ans = 0;
    ll l = 0;
    ll r = N - 1;
    sort(arr.begin(), arr.end());
    while (l <= r)
    {
        if (1LL * arr[r] * 2 > D)
        {
            l++;
            r--;
            ans++;
        }
        else
        {
            break;
        }
    }
    cout << ans << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}

