void solve()
{
    ll n;
    cin >> n;
    vector<ll> arr(n, 0);
    vector<vector<int>> m(4);
    for (ll i = 0; i < n; i++)
    {
        ll temp;
        cin >> temp;

        if (temp % 6 == 0)
        {
            m[0].push_back(temp);
        }
        else if (temp % 2 == 0)
        {
            m[1].push_back(temp);
        }
        else if (temp % 3 == 0)
        {
            m[3].push_back(temp);
        }
        else
        {
            m[2].push_back(temp);
        }
    }

    for (const auto &r : m)
    {
        for (const auto &v : r)
        {
            cout << v << " ";
        }
    }
    cout << "\n";
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

