void solve()
{
    ll x, y;
    cin >> x >> y;

    ll rX = x % 2, rY = y % 2;
    if (rX == 1 && rY == rX)
    {
        cout << "NO\n";
    }
    else
    {
        cout << "YES\n";
    }
}

