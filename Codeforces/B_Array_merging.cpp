void solve()
{
    ll n;
    cin >> n;
    unordered_map<ll, ll> freq;
    for (ll i = 0; i < n; i++)
    {
        ll temp;
        cin >> temp;
        freq[temp]++;
    }
    for (ll i = 0; i < n; i++)
    {
        ll temp;
        cin >> temp;
        freq[temp]++;
    }

    ll ans = 0;
    for (const auto &f : freq)
    {
        ans = max(ans, f.second);
    }
    cout << ans << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

