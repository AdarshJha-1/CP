void solve()
{
    ll n, p;
    cin >> n >> p;
    vector<ll> R(n, 0), C(n, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> R[i];
    }
    ll smallestCost = INT_MAX;
    for (int i = 0; i < n; i++)
    {
        cin >> C[i];
        smallestCost = min(smallestCost, C[i]);
    }
    if (smallestCost <= p)
    {
        cout << p * n << endl;
        return;
    }
    vector<pair<int, int>> vp;
    for (int i = 0; i < n; i++)
    {
        vp.push_back({C[i], R[i]});
    }
    sort(vp.begin(), vp.end());
    ll totalCost = p;
    int i = 0;
    for (; i < n; i++)
    {
        auto [cost, rShare] = vp[i];
        while (rShare--)
        {
            totalCost += cost;
            i++;
        }
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

