void solve()
{
    int n;
    cin >> n;
    vi arr(n, 0);
    vi feq(101, 0);
    int f = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
        feq[arr[i]]++;
        if (feq[arr[i]] == 2)
        {
            f = 1;
        }
    }

    if (f == 1)
    {
        cout << -1 << endl;
        return;
    }
    sort(arr.rbegin(), arr.rend());
    for (const auto &a : arr)
    {
        cout << a << " ";
    }
    cout << endl;
}

