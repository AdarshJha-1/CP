void solve()
{
    vector<int> arr(7, 0);
    for (int i = 0; i < 7; i++)
    {
        cin >> arr[i];
    }
    sort(arr.begin(), arr.end());
    int sum = 0;
    for (int i = 0; i < 6; i++)
    {
        sum -= arr[i];
    }
    cout << sum + arr[6] << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

