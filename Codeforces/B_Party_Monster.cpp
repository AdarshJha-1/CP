void solve()
{
    ll n;
    cin >> n;
    string s;
    cin >> s;
    ll sum = 0;
    for (const auto &c : s)
    {
        if (c == '(')
        {
            sum += 1;
        }
        else
        {
            sum -= 1;
        }
    }

    if (sum == 0)
    {
        cout << "YES\n";
    }
    else
    {
        cout << "NO\n";
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

