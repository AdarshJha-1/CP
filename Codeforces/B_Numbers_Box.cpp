void solve()
{
    int n, m;
    cin >> n >> m;
    vector<vector<int>> arr(n, vector<int>(m, 0));
    int sum = 0;
    int minE = INT_MAX;
    int negC = 0;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> arr[i][j];
            negC += arr[i][j] < 0;
            sum += abs(arr[i][j]);
            minE = min(minE, abs(arr[i][j]));
        }
    }

    cout << (negC % 2 == 0 ? sum : sum - (minE * 2)) << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

