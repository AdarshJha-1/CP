void solve()
{
    int n, r, b;
    cin >> n >> r >> b;
    vector<string> temp(b + 1, "");

    int rC = 0;
    while (rC < r)
    {
        for (auto &s : temp)
        {
            s += "R";
            rC++;
            if (rC >= r)
            {
                break;
            }
        }
    }
    string ans = "";
    for (auto &s : temp)
    {
        ans += s;
        ans += 'B';
    }
    ans.pop_back();
    cout << ans << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

