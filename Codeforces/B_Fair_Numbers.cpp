void solve()
{
    ll n;
    cin >> n;

    long long i = n;
    for (; i < 10e18; i++)
    {
        bool divides = true;
        ll temp = i;
        while (temp)
        {
            int d = temp % 10;
            if (d > 0 && i % d != 0)
            {
                divides = false;
                break;
            }
            temp /= 10;
        }
        if (divides)
        {
            break;
        }
    }
    cout << i << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

