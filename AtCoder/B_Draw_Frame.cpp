void solve()
{
    int H, W;
    cin >> H >> W;
    vector<vector<char>> ans(H, vector<char>(W, '.'));

    for (int i = 0; i < H; i++)
    {
        ans[i][0] = '#';
        ans[i][W - 1] = '#';
    }
    for (int i = 0; i < W; i++)
    {
        ans[0][i] = '#';
        ans[H - 1][i] = '#';
    }

    for (int i = 0; i < H; i++)
    {
        for (int j = 0; j < W; j++)
        {
            cout << ans[i][j];
        }
        cout << endl;
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}

