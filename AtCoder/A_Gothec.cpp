void solve()
{
    int M, D;
    cin >> M >> D;

    vector<pair<int, int>> f{
        {1, 7},
        {3, 3},
        {5, 5},
        {7, 7},
        {9, 9}};

    pair<int, int> i = {M, D};
    bool ans = false;
    for (const auto &d : f)
    {
        if (d == i)
        {
            ans = true;
        }
    }
    if (ans)
    {
        cout << "Yes" << endl;
    }
    else
    {
        cout << "No" << endl;
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}

